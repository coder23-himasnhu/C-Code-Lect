// Array VS LinkedList

// Array:- It stroe data at contiguos memory location
// Insertion and deletion can be inefficient.
// Array ke middle me agar koi element insert karna hai aur array ka size fix hai to possible nhi hai
// Random access is possible.(kisi bhi element ko randomly access karna easy hai)
// Generally has less memory head, it only need to store data.
// eg:- agar hame pata hai ki kitni memory chahiye, to waha hum array ko use kar sakte hai.
// when data size is static(when we know the size of data) then we can use the array.

// Linked List:- Element are not stored in continuos location. Each element contain a refrence to the next element.
// Insertion and deletion is efficient as compare to array.
// Linked list koi fixed size ka nhi hota to hum kahi bhi element ko insert kara sakte hai
// eg:- agar koi web hai uspe visit karne wale user ka data lena hai to waha kaam ayega qki we dont know how many user can visit the webpage.
// Random access is not possible.
// Required additional memory for storing refrence.
// when data size is dynamic then we can use the linked list.